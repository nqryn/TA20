# Operatori comparare: compara numere
# Mai mic (<), mai mare (>), mai mic sau egal (<=), mai mare sau egal (>=)
print(7 > 5)  # True
print(7 < 5)  # False

# Testare egal (==) sau diferit (!=)
a, b = 5, 7
print(a == b)  # False
print(7 == b)  # True
print((a + 2) == b)  # True
print(a != b)  # True
