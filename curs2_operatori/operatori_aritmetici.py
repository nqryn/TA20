a, b = 12, 5

print(a + b)  # 17
print(a - b)  # 7
print(a * b)  # 60
print(a / b)  # Impartire (va da float): 2.4
print(a // b)  # Impartire fara rest (va da int): 2
print(a % b)  # Restul impartirii (modulo): 2
print(a ** b)  # Ridicarea la putere: 248832

# Putem folosi anumiti operatori aritmetici si pe alte tipuri de date (nu doar numere cu numere)
print("Adela " + "Neacsu")  # concatenare, adica stringurile sunt puse unul dupa altul
print("A" + "BC" + "DEF" + "GHIJ")  # putem concatena oricate stringuri

s = "Adela " * 5  # Afisaza stringul Adela de 5 ori
print(s)
